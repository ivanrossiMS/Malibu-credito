-- 3. Nota sobre Backups:
-- O Supabase já faz backups automáticos diários. 
-- Para o "Botão de Backup no Servidor", criaremos uma tabela `backups_history` 
-- para registrar quando um backup manual foi solicitado e o status.

CREATE TABLE IF NOT EXISTS backups_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    requested_by UUID REFERENCES auth.users(id),
    status TEXT DEFAULT 'COMPLETED', -- 'PENDING', 'COMPLETED', 'FAILED'
    storage_path TEXT, -- Link para o arquivo no Supabase Storage se necessário
    file_size BIGINT
);
